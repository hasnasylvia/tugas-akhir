    ada gejala sama sekali sangatlah rendah. Namun, banyak orang yang terjangkit COVID-19 hanya mengalami gejala 
    ringan, terutama pada tahap-tahap awal. Karena itu, COVID-19 dapat menular dari orang yang, misalnya, hanya batuk 
    ringan tetapi merasa sehat.
</p>
