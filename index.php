    virus jenis baru yang ditemukan menyebabkan penyakit COVID-19.
</p>
<h4>Apa itu COVID-19?</h4>
<center><img src="odp/ciri.jpg"></center>
<p>
    Covid-19 adalah penyakit menular yang disebabkan oleh jenis Corona Virus yang baru ditemukan.
    Ini merupakan virus baru dan penyakit yang sebelumnya tidak dikenal sebelum wabah di Wuhan, Tiongkok, bulan
    Desember 2019.
</p>
<h4>Apa saja gejala COVID-19?</h4>
<center><img src="odp/batuk.jpg"></center>
<p>
    Gejala-gejala COVID-19 yang paling umum adalah demam, rasa lelah, dan batuk kering. Beberapa pasien mungkin
    mengalami rasa nyeri dan sakit, hidung tersumbat, pilek, sakit tenggorokan atau diare. Gejala-gejala yang
    dialami mungkin bersifat ringan dan muncul secara bertahap.
</p>
<p>
    Beberapa orang yang terinfeksi tidak menunjukkan gejala apa pun dan tetap merasa sehat. Sebagian besar (sekitar
    80%) orang yang terinfeksi berhasil pulih tanpa perlu perawatan khusus. Sekitar 1 dari 6 orang yang terjangkit
    COVID-19 menderita sakit parah dan kesulitan bernapas. Orang-orang lanjut usia (Lansia) dan orang-orang dengan
    kondisi medis yang sudah ada sebelumnya seperti tekanan darah tinggi, gangguan jantung atau diabetes, punya 
    kemungkinan lebih besar mengalami sakit lebih serius. Mereka yang mengalami demam, batuk dan kesulitan bernapas 
    sebaiknya mencari pertolongan medis.
</p>
<h4>Bagaimana cara COVID-19 menyebar?</h4>
<center><img src="odp/batuk2.jpg"></center>
<p>
    Orang dapat tertular COVID-19 dari orang lain yang terjangkit virus ini. COVID-19 dapat menyebar dari orang ke 
    orang melalui percikan-percikan dari hidung atau mulut yang keluar saat orang yang terjangkit COVID-19 batuk atau
    mengeluarkan napas. Percikan-percikan ini kemudian jatuh ke benda-benda dan permukaan di sekitar. Orang yang 
    menyentuh benda atau permukaan tersebut lalu menyentuh mata, hidung atau mulutnya, dapat tejangkit COVID-19. 
    Penularan COVID-19 juga dapat terjadi jika orang menghirup percikan orang yang keluar dari batuk atau napas orang 
    yang terjangkit COVID-19. Oleh karena itu, penting bagi kita untuk menjaga jarak lebih dari 1 meter dari orang 
    yang sakit.
</p>
<p>
    Apakah virus penyebab COVID-19 ini dapat menular melalui udara? Menurut penelitian sejauh ini, virus penyebab 
    COVID-19 ini umumnya menular melalui kontak dengan percikan dari saluran pernapaasan, bukan melalui udara.
</p>
<p>
    Apakah COVID-19 dapat menular dari orang yang tidak menunjukkan gejala? Cara utama penyebaran penyakit ini adalah 
